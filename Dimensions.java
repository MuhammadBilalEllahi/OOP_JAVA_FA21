public class Dimensions {
    int length, width, height;

    public Dimensions ( int length, int width, int height ){
        this.height = height;
        this.length = length;
        this.width = width;

    }

    public int getHeight() {
        return height;
    }

    public int getLength() {
        return length;
    }

    public int getWidth() {
        return width;
    }
}

