public class Rectangle {
    int width ;
    int length ;

    int x ;
    int y ;

    void setWidth( int w ){
        width = w ;
    }

    int getWidth(){
        return width;
    }

    void setLength( int l ){
        length = l ;
    }

    int getLength(){
        return length;
    }
    void setx( int lx ){
        x = lx ;
    }

    int getx(){
        return x;
    }
    void sety( int ly ) {
        y = ly;
    }

    int gety(){
        return y;
    }


}
